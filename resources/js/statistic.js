function callDatabase(houseId) {
    $.ajax({
        "url": "http://localhost:8000/api/statistic",
        "data": {
            "id": houseId
        },
        "method": "GET",
        "success": function (data) {
            // console.log(data);
            printResults(data);
        },
        "error": function (err) {
            alert("Error");
        }
    });
}

